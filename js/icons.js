        return `<svg viewBox="0 0 100 100" class="w-16 h-16"><path d="M50 10 A40 40 0 1 1 50 90 A40 40 0 1 0 50 10" fill="${bgFill}" stroke="currentColor" stroke-width="2"/><path d="M50 10 A40 40 0 0 1 50 90 A30 40 0 0 0 50 10" fill="${litFill}"/></svg>`;
